# Возведение в степень


z = input("Введите числа через пробел: ").strip()
zn = z.split()

while True:
    s = input("Введите степень: ").strip()
    if s.lstrip('-').isdigit():
        st = int(s)
        break
    else:
        print("Ошибка: степень должна быть целым числом")

v = []
for voz in zn:
    if voz.replace('.', '', 1).lstrip('-').isdigit():
        voz = float(voz)
        voz = voz ** st
        voz = int(voz) if voz % 1 == 0 else round(voz, 5)
        v.append(str(voz))
    else:
        st = -st if st < 0 else st
        voz = voz * st
        v.append(voz)

print(f"Вывод:{" ".join(v)}")

