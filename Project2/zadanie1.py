# метод split не рассматривается в лекции или справочнике, но он есть в источниках в ссылке set()
# и он Return a list of the lines in the string те разбивает введенную строку на слова(в нашем
# случае по пробелам)
# далее используя цикл делаем проверку введенного значения для степени
# если введено не число, программа попросит его ввести без прерывания работы программы
# я также решила оставить возможность ввести отрицательную степень, при этом число должно быть целым
# для введения числа с плавающей точкой нужно использовать комплексные число
# что пока что не разбиралось
# далее мы создаем цикл для возведения в заданную степень перебирая каждое введенное значение
# если оно число то возводим его в степень и с помощью условия либо приводим к типу инт (чтобы не было число с
# плавающей запятой напр 1.0) либо если число с плав. запятой то округляем до 5 цифр после запятой
# далее с помощью append добавляем элемент в конец списка при этом преобразуя его в строковый тип
# если элемент не число то мы с помощью условия если степень отрицательная делаем ее положительной
# или оставляем как есть и дублируем строку в количестве степени
